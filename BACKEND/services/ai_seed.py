def seed_db():
    db = SessionLocal()
    questions = json.loads(THEORY_DATA)
    added_count = 0

    print("🌱 Ξεκινάει η προσθήκη ερωτήσεων θεωρίας...")
    
    try:
        for q_data in questions:
            # 🔥 ΕΞΥΠΝΗ ΑΝΑΖΗΤΗΣΗ: Βρίσκουμε το UUID του κεφαλαίου από το order_num
            chapter_number = int(q_data["chapter_id"])
            chapter_db = db.query(Chapter).filter(Chapter.order_num == chapter_number).first()
            
            if not chapter_db:
                print(f"⚠️ Προσοχή: Δεν βρέθηκε Κεφάλαιο {chapter_number} στη βάση. Προσπερνάω αυτή την ερώτηση...")
                continue
                
            real_chapter_uuid = chapter_db.id

            # Έλεγχος για διπλοεγγραφές
            exists = db.query(TheoryQuestion).filter(TheoryQuestion.question_text == q_data["question_text"]).first()
            
            if not exists:
                new_question = TheoryQuestion(
                    chapter_id=real_chapter_uuid, # Βάζουμε το πραγματικό UUID της βάσης!
                    question_text=q_data["question_text"],
                    ideal_answer=q_data["ideal_answer"],
                    keywords=q_data["keywords"]
                )
                db.add(new_question)
                added_count += 1
                print(f"✅ Προστέθηκε: {q_data['question_text'][:40]}...")
            else:
                print(f"⏩ Υπάρχει ήδη: {q_data['question_text'][:40]}...")

        db.commit()
        print(f"\n🎉 Ολοκληρώθηκε! Προστέθηκαν {added_count} νέες ερωτήσεις στη βάση.")

    except Exception as e:
        db.rollback()
        print(f"❌ Σφάλμα: {e}")
    finally:
        db.close()

if __name__ == "__main__":
    seed_db()